void ShadowMapping::drawScene(Shader& curShader){
    // floor
    glm::mat4 model = glm::mat4(1.0f);
    curShader.setValue("model", model);
    glBindVertexArray(planeVAO);
    glDrawArrays(GL_TRIANGLES, 0, 6);
    // cubes
    model = glm::mat4(1.0f);
    model = glm::translate(model, glm::vec3(0.0f, 1.5f, 0.0));
    model = glm::scale(model, glm::vec3(0.5f));
    curShader.setValue("model", model);
    drawCube();
    model = glm::mat4(1.0f);
    model = glm::translate(model, glm::vec3(2.0f, 0.0f, 1.0));
    model = glm::scale(model, glm::vec3(0.5f));
    curShader.setValue("model", model);
    drawCube();
    model = glm::mat4(1.0f);
    model = glm::translate(model, glm::vec3(-1.0f, 0.0f, 2.0));
    model = glm::rotate(model, glm::radians(60.0f), glm::normalize(glm::vec3(1.0, 0.0, 1.0)));
    model = glm::scale(model, glm::vec3(0.25));
    curShader.setValue("model", model);
    drawCube();
}

void ShadowMapping::draw(Camera& camera){
    glm::mat4 lightSpaceMatrix = lightCamera->getProjectionMatrix()*lightCamera->getCameraPoseMatrix();

    // 1. render to depth map
    shaderDepth->use();
    shaderDepth->setValue("lightSpaceMatrix",lightSpaceMatrix);
    glViewport(0, 0, SHADOW_WIDTH, SHADOW_HEIGHT);
    glBindFramebuffer(GL_FRAMEBUFFER, depthMapFBO);
    glCullFace(GL_FRONT);
        glClear(GL_DEPTH_BUFFER_BIT);
        glActiveTexture(GL_TEXTURE0);
        glBindTexture(GL_TEXTURE_2D, floorTexture->id);
        drawScene(*shaderDepth);
    glCullFace(GL_BACK);
    glBindFramebuffer(GL_FRAMEBUFFER, 0);  


    // 2. then render scene as normal with shadow mapping (using depth map)
    glViewport(0, 0, EngineWindow::getInstance().viewportWidth, EngineWindow::getInstance().viewportHeight);
    glClear(GL_COLOR_BUFFER_BIT | GL_DEPTH_BUFFER_BIT);
    // ConfigureShaderAndMatrices();
    shader->use();
    shader->setValue("lightSpaceMatrix",lightSpaceMatrix);
    shader->setValue("diffuseTexture",0);
    shader->setValue("shadowMap",1);
    shader->setValue("lightPos",lightCamera->cameraPosition);
    shader->setValue("viewPos",camera.cameraPosition);
    glActiveTexture(GL_TEXTURE0);
    glBindTexture(GL_TEXTURE_2D,floorTexture->id);
    glActiveTexture(GL_TEXTURE1);
    glBindTexture(GL_TEXTURE_2D, depthMap);
    
    drawScene(*shader);
    glActiveTexture(GL_TEXTURE0);
    // RenderScene();
    
}
